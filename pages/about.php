<h1>Where &amp; When</h1>
<p>
May 1-4 2014, <a href="http://zkm.de/" rel="external">ZKM</a> (<span lang="de" xml:lang="de">Zentrum f&uuml;r Kunst &amp; Medientechnologie</span> - Centre for Art and Media), Karlsruhe, Germany.
