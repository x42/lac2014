<h1>About</h1>
<p>
The Linux Audio Conference is the international conference about
Free/Open-Source Software for music, sound and other media with GNU/Linux as the main platform.
The 2014 edition (the twelfth installment of the conference) will take place at the <em>ZKM</em>, Karlsruhe, Germany, the place where it all started.
</p>

<h1>Target audience</h1>
<p>
The Linux Audio Conference brings together musicians, composers, sound artists, software developers, researchers and engineers working with GNU/Linux as an open, stable, professional platform for audio and media research and music production.
</p>

<h1>What to expect</h1>
<ul class="multicolumn">
 <li>Paper presentations</li>
 <li>Poster sessions</li>
 <li>Workshops</li>
 <li>Concerts</li>
 <li>Sound Installations</li>
 <li>Live coding sessions</li>
</ul>
