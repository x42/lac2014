<h1>Excursion</h1>                                                                                                                             

<p>
<em>Will you still be around Sunday afternoon?</em>
<br/>
The final event of the conference will be a trip to the countryside to a small village in the &laquo;Kranichgau&raquo;, a hilly region in between the Black Forest and Odenwald about 40 minutes east of Karlsruhe.
<br/>
We'll visit the <a href="https://en.wikipedia.org/wiki/Maulbronn_Monastery">Kloster Maulbronn</a> which is the best-preserved medieval Cistercian monastery complex in Europe. Apart from its architectural diversity (Romanesque art to late Gothic) it features a unique acoustic, not to mention a brewery with refreshing Kloster-bier.
</p>

